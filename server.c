#include "minitalk.h"

void	ft_print(int sig)
{
	static int	i;
	static char	c;

	if (sig == SIGUSR1)
		c += 1 << i;
	else
		c += 0 << i;
	i++;
	if (i == 8)
	{
		write(1, &c, 1);
		i = 0;
		c = 0;
	}
}

